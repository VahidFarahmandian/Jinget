namespace Jinget.Core.Utilities;

/// <summary>
/// Provides utility methods for boolean logic.
/// </summary>
public static class HumanizerUtility
{
    /// <summary>
    /// Negates a boolean value.
    /// </summary>
    /// <param name="possibility">The boolean value to negate.</param>
    /// <returns>The negated boolean value.</returns>
    public static bool Not(bool possibility) => !possibility;
}